def get_region_coords(full_name: str):
    """Get (lat, lon) for a region. Falls back to country centroid."""
    if full_name in _ADMIN1_COORDS:
        return _ADMIN1_COORDS[full_name]
    # Try partial match
    for key, coords in _ADMIN1_COORDS.items():
        if full_name.upper() in key.upper() or key.upper() in full_name.upper():
            return coords
    # Country-level fallback
    for country, coords in _COUNTRY_COORDS.items():
        if country.upper() in full_name.upper():
            return coords
    return None


def get_all_coords():
    """Return all available coordinates."""
    return _ADMIN1_COORDS.copy()
